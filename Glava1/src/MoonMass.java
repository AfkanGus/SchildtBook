public class MoonMass {
    public static void main(String[] args) {
        double massToMoon, massToEarch;

        massToEarch = 83;
        massToMoon = 83 * 17 / 100;

        System.out.println("Ваша земная масса " + massToEarch + " кг. на Луне изменится до " + massToMoon  + " кг.");

    }
}
